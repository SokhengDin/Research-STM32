void updateMotorControl(void)
{
	// Motor 1
	int16_t pwm1 = rpmToPwm(motor_speed1);

	if (pwm1 >=0)
	{
		TIM4->CCR1 = pwm1;
		TIM4->CCR2 = 0;
	}
	else
	{
		TIM4->CCR1 = 0;
		TIM4->CCR2 = pwm1;
	}

	// Motor 2
	int16_t pwm2 = rpmToPwm(motor_speed2);

	if (pwm2 >=0)
	{
		TIM4->CCR3 = pwm2;
		TIM4->CCR4 = 0;
	}
	else
	{
		TIM4->CCR3 = 0;
		TIM4->CCR4 = pwm2;
	}
}


