int16_t rpmToPwm(int16_t rpm)
{
    // Conversion
    int32_t pwm = (int32_t)rpm * (PWM_PERIOD - 1) / MAX_RPM;
    if (pwm > (PWM_PERIOD - 1)) pwm = PWM_PERIOD - 1;
    else if (pwm < -(PWM_PERIOD - 1)) pwm = -(PWM_PERIOD - 1);

    return (int16_t)pwm;
}





